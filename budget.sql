CREATE TABLE BUDGET(
    BUDGET_ID VARCHAR(20) NOT NULL PRIMARY KEY,
    TOTAL_REVENUE VARCHAR(20),
    TOTAL_EXPENDITURE VARCHAR(20),
    GDP_GROWTH_RATE FLOAT,
    DEBT_GDP_RATIO FLOAT,
    FOREX  FLOAT
);

CREATE TABLE COUNTRIES(
    COUNTRY_ID VARCHAR(20) NOT NULL PRIMARY KEY,
    TOTAL_GDP VARCHAR(20),
    DEBT_GDP_RATIO_RATIO FLOAT,
    FOREX FLOAT
);

CREATE TABLE PROGRAM(
    PROG_ID VARCHAR(20) NOT NULL PRIMARY KEY,
    PROG_NAME VARCHAR(20)
);

CREATE TABLE SCHEME(
    SCHEME_ID VARCHAR(20) NOT NULL PRIMARY KEY,
    SCHEME_NAME VARCHAR(140),
    PROG_ID VARCHAR(20),
    FOREIGN KEY(PROG_ID) REFERENCES PROGRAM(PROG_ID)
);

CREATE TABLE BENEFICIARY(
    BENI_ID VARCHAR(20) NOT NULL PRIMARY KEY,
    BENI_NAME VARCHAR(100),
    BENI_TYPE VARCHAR(100),
    ADDRESS VARCHAR(100),
    SCHEME_ID VARCHAR(20),
    FOREIGN KEY(SCHEME_ID) REFERENCES SCHEME(SCHEME_ID)
);

CREATE TABLE PROVIDES_FOR(
    BENI_ID VARCHAR(20),
    SCHEME_ID VARCHAR(20),
    FOREIGN KEY(BENI_ID) REFERENCES BENEFICIARY(BENI_ID),
    FOREIGN KEY(SCHEME_ID) REFERENCES SCHEME(SCHEME_ID),
    PRIMARY KEY(BENI_ID,SCHEME_ID)
);

CREATE TABLE STATES(
    STATE_ID VARCHAR(20) NOT NULL PRIMARY KEY,
    PROG_ID VARCHAR(20),
    SCHEME_ID VARCHAR(20),
    BENI_ID VARCHAR(20),
    ALLOTED_BUDGET VARCHAR(40),
    FOREIGN KEY(PROG_ID) REFERENCES PROGRAM(PROG_ID),
    FOREIGN KEY(SCHEME_ID) REFERENCES SCHEME(SCHEME_ID),
    FOREIGN KEY(BENI_ID) REFERENCES BENEFICIARY(BENI_ID)   
);

CREATE TABLE DEPARTMENT(
    DEPT_ID VARCHAR(20) NOT NULL PRIMARY KEY,
    DEPT_NAME VARCHAR(100),
    DEPT_BUDGET VARCHAR(40),
    PROG_ID VARCHAR(20),
    FOREIGN KEY(PROG_ID) REFERENCES PROGRAM(PROG_ID)
);

CREATE TABLE ADMINISTERS(
    DEPT_ID VARCHAR(20),
    PROG_ID VARCHAR(20),
    FOREIGN KEY(DEPT_ID) REFERENCES DEPARTMENT(DEPT_ID),
    FOREIGN KEY(PROG_ID) REFERENCES PROGRAM(PROG_ID),
    PRIMARY KEY(DEPT_ID,PROG_ID)
);

CREATE TABLE EXPENDITURE(
    EXP_ID VARCHAR(100) NOT NULL PRIMARY KEY,
    EXPENDITURE_CAT_ID VARCHAR(20),
    DEPT_ID VARCHAR(20),
    PROG_ID VARCHAR(20),
    FOREIGN KEY(DEPT_ID) REFERENCES DEPARTMENT(DEPT_ID),
    FOREIGN KEY(PROG_ID) REFERENCES PROGRAM(PROG_ID),
    FOREIGN KEY(EXPENDITURE_CAT_ID) REFERENCES EXPENDITURE_CATEGORY(EXPENDITURE_CAT_ID)
);

CREATE TABLE EXPENDITURE_CATEGORY(
    EXPENDITURE_CAT_ID VARCHAR(20) NOT NULL PRIMARY KEY,
    AMOUNT VARCHAR(100),
    TYPE VARCHAR(100)
);

CREATE TABLE TAXATION(
    TAX_ID VARCHAR(20) NOT NULL PRIMARY KEY,
    TAX_REVENUE VARCHAR(100),
    TAX_TYPE VARCHAR(100),
    EXP_ID VARCHAR(100),
    FOREIGN KEY(EXP_ID) REFERENCES EXPENDITURE(EXP_ID)  
);

